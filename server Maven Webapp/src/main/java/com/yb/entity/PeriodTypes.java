package com.yb.entity;

public class PeriodTypes {
	private Integer Period_Type;
	private String Period_name;
	private String Period_description;
	private Integer Period_keep_days;
	public Integer getPeriod_Type() {
		return Period_Type;
	}
	public void setPeriod_Type(Integer period_Type) {
		Period_Type = period_Type;
	}
	public String getPeriod_name() {
		return Period_name;
	}
	public void setPeriod_name(String period_name) {
		Period_name = period_name;
	}
	public String getPeriod_description() {
		return Period_description;
	}
	public void setPeriod_description(String period_description) {
		Period_description = period_description;
	}
	public Integer getPeriod_keep_days() {
		return Period_keep_days;
	}
	public void setPeriod_keep_days(Integer period_keep_days) {
		Period_keep_days = period_keep_days;
	}
	
}
